                    <div id="demo" class="collapse">
                        <span>  
                        <br>
                        <p>
                            El método mas sencillo y rápido es envolver un cajón de verduras en una bolsa de residuo 
                            gruesa, adentro hacemos una capa de 5 cm aprox. (3 dedos) de cartón/papel de diario cortado en 
                            tiritas en una mitad del cajón (y dejamos la otra libre), luego ponemos los desechos de 
                            verduras de la cocina en una capa de 2 cm (1 dedo gordo), luego otra capa de cartón de 2 cm y así hasta 
                            llegar a la mitad del cajón (en altura), regamos bastante.
                            Lo suficiente como para mojar todo, pero no para que quede mucho liquido en el fondo 
                            (por eso es la mitad del cajón), lo cubrimos con una remera vieja o con la misma bolsa 
                            y lo dejamos 48 hs.
                            Pasado ese tiempo revisamos: 
                        </p>
                        <ul class="list-group">
                                <li class="list-group-item">
                                    Hay olor desagradable? --> remover/agregar mas cartón/chequear humedad.
                                </li>
                                <li class="list-group-item">
                                    Esta baboso el contenido? --> Remover/agregar mas cartón/chequear humedad.
                                </li>
                                <li class="list-group-item">
                                    Hay cambios en como se ve? Se ve seco? --> Agregar agua.
                                </li>
                        </ul>
                        <hr>
                        <p>
                        Al cuarto/quinto día, ya debería estar bajando la temperatura de la masa. No debería haber olores fuertes 
                        (aunque un poco es normal, hay zonas que no se airean bien). Completar el cajón con mas cartón, remojar, 
                        hacerle unos agujeros de 0,5 mm (tamaño de lápiz aprox) a nuestra bolsita de lombrices VermiBA con mucho 
                        cuidado y depositarla en una esquina contraria a donde esta nuestro compost.
                        <br>        
                        Si salio todo bien en 24/48 hs deberían estar comiendo todo! <br>
                        Ahora la parte mas difícil de todas...PACIENCIA...al ser un nuevo lugar, van a tardar en procesar toooda la comida que les dimos. 
                        Esto puede llevar <i>1 semanas o mas</i> (dependiendo de la cantidad de lombrices obviamente). 
                        Controlar cada dos/tres días temperatura y humedad, molestando lo menos posible. <br> 
                        Pasado este tiempo: Notaran que no se pueden identificar los restos de comida, y que hay mucho humus de lombriz en la superficie.
                        Es momento de darles de comer!: Sobre un cuarto del cajón donde dejaron la 
                        <a href="index.php#contacto">Bolsita VermiBA</a> (osea del lado contrario a donde hicieron la pila) 
                        aplicar una capa de aprox 2-3 cms de desechos (siempre sumen residuos marrones). Y a repetir el ciclo!<br>
                        Lo siguiente que queda hacer es pensar a futuro.
                        </p>
